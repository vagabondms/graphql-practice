import { GraphQLObjectType, GraphQLID, GraphQLString } from "graphql";

const Client = new GraphQLObjectType({
  name: "Client",
  fields: () => ({
    id: { type: GraphQLID },
    name: { type: GraphQLString },
    email: { type: GraphQLString },
    phone: { type: GraphQLString },
  }),
});

export default Client;
